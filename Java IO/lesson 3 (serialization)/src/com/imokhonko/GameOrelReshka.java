package com.imokhonko;

public class GameOrelReshka {

    private final float orel;
    private final float reshka;

    public GameOrelReshka(float orel, float reshka) {
        this.orel = orel;
        this.reshka = reshka;
    }

    public float getOrel() {
        return orel;
    }

    public float getReshka() {
        return reshka;
    }
}
